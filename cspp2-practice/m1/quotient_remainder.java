import java.util.*;
class quotient_remainder{
	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);
		System.out.print("a=");
		int a = s. nextInt();
		System.out.print("b=");
		int b = s. nextInt();
		System.out.println("Remainder ="+(a%b)); 
		System.out.println("Quotient ="+(a/b)); 

	}
}